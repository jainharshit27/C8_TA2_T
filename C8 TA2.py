def math_steps(a,b):
    #Add A & B. Result = C
    c = a + b
    
    #Multiply C with B. Result = D
    d = c * b
    
    #Divide D by A. Result = E
    e = d / a
    
    #Subtract E from D. Result = F
    f = d - e
    
    #Answer = F. use either print() or return keyword
    print(f)        



math_steps(5,7)








